export function validateUpgrades(upgradeList = upgrades) {
  const ids = new Set();
  const issues = [];

  upgradeList.forEach((u, index) => {
    if (!u.id) issues.push(`Upgrade at index ${index} missing id`);
    if (ids.has(u.id)) issues.push(`Duplicate upgrade id: ${u.id}`);
    ids.add(u.id);
    if (!u.position || typeof u.position.x !== 'number' || typeof u.position.y !== 'number') {
      issues.push(`Upgrade ${u.id} has invalid position`);
    }
    if (!u.size || typeof u.size.w !== 'number' || typeof u.size.h !== 'number') {
      issues.push(`Upgrade ${u.id} has invalid size`);
    }
    if (!Array.isArray(u.prerequisites)) {
      issues.push(`Upgrade ${u.id} prerequisites must be an array`);
    }
  });

  upgradeList.forEach((u) => {
    u.prerequisites.forEach((req) => {
      if (!ids.has(req)) {
        issues.push(`Upgrade ${u.id} has unknown prerequisite: ${req}`);
      }
    });
  });

  return {
    valid: issues.length === 0,
    issues
  };
}
